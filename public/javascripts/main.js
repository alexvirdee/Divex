$(document).ready(function() {
	$('.delete-dive').on('click', function(e) {
		$target = $(e.target);
		const id = $target.attr('data-id');
		$.ajax({
			type: 'DELETE',
			url: '/api/dives/' + id,
			success: function(response) {
				window.location.href='/api/dives'
			},
			error: function() {
				console.log('error');
			}
		});
	});
});

